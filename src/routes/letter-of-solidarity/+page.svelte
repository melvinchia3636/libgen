<section class="w-full flex-1 flex justify-center">
	<div class="w-3/5 py-32">
		<h1 class="text-5xl font-['Edu_NSW_ACT_Foundation']">
			In solidarity with <u class="font-['Edu_NSW_ACT_Foundation']">Library Genesis</u> and
			<u class="font-['Edu_NSW_ACT_Foundation']">Sci-Hub</u>
		</h1>

		<p>
			In Antoine de Saint Exupéry's tale the Little Prince meets a businessman who accumulates stars
			with the sole purpose of being able to buy more stars. The Little Prince is perplexed. He owns
			only a flower, which he waters every day. Three volcanoes, which he cleans every week. "<i
				>It is of some use to my volcanoes, and it is of some use to my flower, that I own them,</i
			>" he says, "<i>but you are of no use to the stars that you own</i>".
		</p>
		<p>
			There are many businessmen who own knowledge today. Consider Elsevier, the largest scholarly
			publisher, whose 37% profit margin <sup
				id="fnref-1"
				class="underline text-orange-500 font-medium">1</sup
			>
			stands in sharp contrast to the rising fees, expanding student loan debt and poverty-level wages
			for adjunct faculty. Elsevier owns some of the largest databases of academic material, which are
			licensed at prices so scandalously high that even Harvard, the richest university of the global
			north, has complained that it cannot afford them any longer. Robert Darnton, the past director
			of Harvard Library, says "<i
				>We faculty do the research, write the papers, referee papers by other researchers, serve on
				editorial boards, all of it for free … and then we buy back the results of our labour at
				outrageous prices.</i
			>" <sup id="fnref-2" class="underline text-orange-500 font-medium">2</sup>
			For all the work supported by public money benefiting scholarly publishers, particularly the peer
			review that grounds their legitimacy, journal articles are priced such that they prohibit access
			to science to many academics - and all non-academics - across the world, and render it a token
			of privilege. <sup id="fnref-3" class="underline text-orange-500 font-medium">3</sup>
		</p>
		<p>
			Elsevier has recently filed a copyright infringement suit in New York against Science Hub and
			Library Genesis claiming millions of dollars in damages. <sup
				id="fnref-4"
				class="underline text-orange-500 font-medium">4</sup
			> This has come as a big blow, not just to the administrators of the websites but also to thousands
			of researchers around the world for whom these sites are the only viable source of academic materials.
			The social media, mailing lists and IRC channels have been filled with their distress messages,
			desperately seeking articles and publications.
		</p>
		<p>
			Even as the New York District Court was delivering its injunction, news came of the entire
			editorial board of highly-esteemed journal Lingua handing in their collective resignation,
			citing as their reason the refusal by Elsevier to go open access and give up on the high fees
			it charges to authors and their academic institutions. As we write these lines, a petition is
			doing the rounds demanding that Taylor & Francis doesn't shut down Ashgate <sup
				id="fnref-5"
				class="underline text-orange-500 font-medium">5</sup
			>
			, a formerly independent humanities publisher that it acquired earlier in 2015. It is threatened
			to go the way of other small publishers that are being rolled over by the growing monopoly and
			concentration in the publishing market. These are just some of the signs that the system is broken.
			It devalues us, authors, editors and readers alike. It parasites on our labor, it thwarts our service
			to the public, it denies us access.
			<sup id="fnref-6" class="underline text-orange-500 font-medium">6</sup>
		</p>
		<p>
			We have the means and methods to make knowledge accessible to everyone, with no economic
			barrier to access and at a much lower cost to society. But closed access’s monopoly over
			academic publishing, its spectacular profits and its central role in the allocation of
			academic prestige trump the public interest. Commercial publishers effectively impede open
			access, criminalize us, prosecute our heroes and heroines, and destroy our libraries, again
			and again. Before Science Hub and Library Genesis there was Library.nu or Gigapedia; before
			Gigapedia there was textz.com; before textz.com there was little; and before there was little
			there was nothing. That's what they want: to reduce most of us back to nothing. And they have
			the full support of the courts and law to do exactly that. <sup
				id="fnref-7"
				class="underline text-orange-500 font-medium">7</sup
			>
		</p>
		<p>
			In Elsevier's case against Sci-Hub and Library Genesis, the judge said: "<i
				>simply making copyrighted content available for free via a foreign website, disserves the
				public interest</i
			>" <sup id="fnref-8" class="underline text-orange-500 font-medium">8</sup> . Alexandra
			Elbakyan's original plea put the stakes much higher: "<i
				>If Elsevier manages to shut down our projects or force them into the darknet, that will
				demonstrate an important idea: that the public does not have the right to knowledge.</i
			>"
		</p>
		<p>
			We demonstrate daily, and on a massive scale, that the system is broken. We share our writing
			secretly behind the backs of our publishers, circumvent paywalls to access articles and
			publications, digitize and upload books to libraries. This is the other side of 37% profit
			margins: our knowledge commons grows in the fault lines of a broken system. We are all
			custodians of knowledge, custodians of the same infrastructures that we depend on for
			producing knowledge, custodians of our fertile but fragile commons. To be a custodian is, de
			facto, to download, to share, to read, to write, to review, to edit, to digitize, to archive,
			to maintain libraries, to make them accessible. It is to be of use to, not to make property
			of, our knowledge commons.
		</p>
		<p>
			More than seven years ago Aaron Swartz, who spared no risk in standing up for what we here
			urge you to stand up for too, wrote: "<i
				>We need to take information, wherever it is stored, make our copies and share them with the
				world. We need to take stuff that's out of copyright and add it to the archive. We need to
				buy secret databases and put them on the Web. We need to download scientific journals and
				upload them to file sharing networks. We need to fight for Guerilla Open Access. With enough
				of us, around the world, we'll not just send a strong message opposing the privatization of
				knowledge — we'll make it a thing of the past. Will you join us?</i
			>
			" <sup id="fnref-9" class="underline text-orange-500 font-medium">9</sup>
		</p>
		<p>
			We find ourselves at a decisive moment. This is the time to recognize that the very existence
			of our massive knowledge commons is an act of collective civil disobedience. It is the time to
			emerge from hiding and put our names behind this act of resistance. You may feel isolated, but
			there are many of us. The anger, desperation and fear of losing our library infrastructures,
			voiced across the internet, tell us that. This is the time for us custodians, being dogs,
			humans or cyborgs, with our names, nicknames and pseudonyms, to raise our voices.
		</p>
		<p>
			Share this letter - read it in public - leave it in the printer. Share your writing - digitize
			a book - upload your files. Don't let our knowledge be crushed. Care for the libraries - care
			for the metadata - care for the backup. Water the flowers - clean the volcanoes.
		</p>

